void RenderObject::initialise(Mesh& mesh)
{
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	GLuint vbo;
	glGenBuffers(1, &vbo);
	glBindBuffer(GL_ARRAY_BUFFER, vbo);
	glBufferData(GL_ARRAY_BUFFER, sizeof(Vertex) * mesh.vertices.size(), mesh.vertices.data(), GL_STATIC_DRAW);

	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)0);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);
	glVertexAttribPointer(2, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)(6 * sizeof(float)));
	glEnableVertexAttribArray(2);

	indicesCount = mesh.indicesCount;

}

void RenderObject::set_render_state(bool state)
{
	renderEnabled = state;
}


void RenderObject::render()
{
	if (!renderEnabled)
	{
		return;
	}

	glBindVertexArray(VAO);
	glDrawArrays(GL_TRIANGLES, 0, indicesCount);
	
}

bool RenderObject::get_render_state()
{
	return renderEnabled;
}
